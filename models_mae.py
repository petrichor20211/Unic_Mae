        N, L, D = img_canny_large_margin.shape

        ids_shuffle_small,len_keep = self.Obtain_sequence(img_canny_samll_margin, mask_ratio)
        ids_shuffle_large,len_keep = self.Obtain_sequence(img_canny_large_margin, mask_ratio)

        ids_shuffle = self.process_tensors(A=ids_shuffle_small,B=ids_shuffle_large,L=L)
        ids_shuffle = ids_shuffle.unsqueeze(dim=0)
        ids_restore = torch.argsort(ids_shuffle, dim=1)
        ids_keep = ids_shuffle[:, :len_keep]
        x_masked = torch.gather(x, dim=1, index=ids_keep.unsqueeze(-1).repeat(1, 1, D))
        # generate the binary mask: 0 is keep, 1 is remove
        mask = torch.ones([N, L], device=x.device)
        mask[:, :len_keep] = 0
        # unshuffle to get the binary mask
        mask = torch.gather(mask, dim=1, index=ids_restore)

